/**
 * Ogilvy repository — CRUD for the autopilot_sessions + autopilot_messages
 * tables (table names predate the rename and are intentionally preserved for
 * backwards-compatible storage).
 *
 * Scope is intentionally narrower than the legacy orchestrator.repository.js:
 * no phase_results, no feedback gates. A session holds a chat transcript and
 * (once drafted) a single plan_json blob.
 */
import crypto from 'node:crypto';
import supabase from '../supabase.js';

// ── Sessions ────────────────────────────────────────────────────────────

export async function createSession({ tenantId, userId = null, title = null, productLine }) {
  if (!tenantId) throw new Error('createSession: tenantId required');
  if (!productLine) throw new Error('createSession: productLine required');
  const { data, error } = await supabase
    .from('autopilot_sessions')
    .insert({ tenant_id: tenantId, user_id: userId, title, status: 'active', product_line: productLine })
    .select()
    .single();
  if (error) throw error;
  return data;
}

// Soft-deleted sessions (`deleted_at IS NOT NULL`) are hidden from all reads
// here — getSession returns null, listSessions skips them. The route layer
// treats a missing session as 404, which is what we want post-delete.
//
// If you need to inspect or restore a soft-deleted row, query the table
// directly via supabase-admin; there's deliberately no `includeDeleted`
// flag in this repo since every business code path should see the
// post-delete state.
export async function getSession(sessionId) {
  const { data, error } = await supabase
    .from('autopilot_sessions')
    .select('*')
    .eq('id', sessionId)
    .is('deleted_at', null)
    .single();
  if (error && error.code !== 'PGRST116') throw error;
  return data;
}

export async function listSessions({ tenantId, userId = null, limit = 50 }) {
  if (!tenantId) throw new Error('listSessions: tenantId required');
  let query = supabase
    .from('autopilot_sessions')
    .select('id, title, status, plan_json, meta_campaign_ids, product_line, created_at, updated_at')
    .eq('tenant_id', tenantId)
    .is('deleted_at', null)
    .neq('status', 'archived')
    .order('updated_at', { ascending: false })
    .limit(limit);
  if (userId) query = query.eq('user_id', userId);
  const { data, error } = await query;
  if (error) throw error;
  return data || [];
}

export async function updateSession(sessionId, updates) {
  const allowed = {};
  for (const key of ['title', 'status', 'plan_json', 'meta_campaign_ids']) {
    if (updates[key] !== undefined) allowed[key] = updates[key];
  }
  const { data, error } = await supabase
    .from('autopilot_sessions')
    .update(allowed)
    .eq('id', sessionId)
    .select()
    .single();
  if (error) throw error;
  return data;
}

/**
 * Append a stage-output record to autopilot_sessions.stage_outputs jsonb array.
 *
 * Read-modify-write — ogilvy 是 single-user single-session 顺序对话，并发写
 * 同一行的概率极低；真出现的话最坏结果是一条记录被覆盖，模型下一轮检测到
 * 缺失会重新调用 persist_stage_output，可以接受。
 *
 * Returns the appended record { id, label, summary, markdown, created_at }
 * with id generated by the DB-friendly randomUUID. Caller doesn't need to
 * pass id; we generate one so the row is referenceable.
 */
export async function appendStageOutput(sessionId, { label, summary, markdown }) {
  const { data: row, error: readErr } = await supabase
    .from('autopilot_sessions')
    .select('stage_outputs')
    .eq('id', sessionId)
    .single();
  if (readErr) throw readErr;

  const record = {
    id: crypto.randomUUID(),
    label: String(label || '').slice(0, 200),
    summary: String(summary || '').slice(0, 1000),
    markdown: String(markdown || ''),
    created_at: new Date().toISOString(),
  };
  const next = Array.isArray(row?.stage_outputs) ? [...row.stage_outputs, record] : [record];

  const { error: writeErr } = await supabase
    .from('autopilot_sessions')
    .update({ stage_outputs: next })
    .eq('id', sessionId);
  if (writeErr) throw writeErr;

  return record;
}

// Atomic status transition: single UPDATE…WHERE status IN (fromStatuses)
// RETURNING. Used by /launch to prevent two concurrent POSTs from both
// staging a campaign tree on Meta. Returns the new row on success, or
// { conflict: { current_status } } if the row was not in an allowed
// from-status (or the session doesn't exist / is soft-deleted).
export async function transitionSessionStatus(sessionId, { from, to, extraUpdates = {} }) {
  const fromList = Array.isArray(from) ? from : [from];

  const allowed = { status: to };
  for (const key of ['plan_json', 'meta_campaign_ids']) {
    if (extraUpdates[key] !== undefined) allowed[key] = extraUpdates[key];
  }

  const { data, error } = await supabase
    .from('autopilot_sessions')
    .update(allowed)
    .eq('id', sessionId)
    .is('deleted_at', null)
    .in('status', fromList)
    .select()
    .single();

  // PGRST116 = no rows matched. Re-read the current status so the caller
  // can return a meaningful 409 body instead of a bare "conflict".
  if (error && error.code === 'PGRST116') {
    const { data: current } = await supabase
      .from('autopilot_sessions')
      .select('status')
      .eq('id', sessionId)
      .is('deleted_at', null)
      .single();
    return { conflict: { current_status: current?.status ?? null } };
  }
  if (error) throw error;
  return data;
}

// Soft-delete: flag the session as deleted instead of removing the row.
// Cascading hard-deletes were the original behaviour, but an accidental bulk
// DELETE on 2026-05-11 wiped every message in the table via the FK cascade
// — see supabase/migrations/2026-05-12-autopilot-soft-delete.sql for the
// full incident note. The row (plus its messages and plan_json) stays in
// place; reads filter on deleted_at IS NULL so the user sees the same
// "gone" UX as before.
export async function deleteSession(sessionId) {
  const { error } = await supabase
    .from('autopilot_sessions')
    .update({ deleted_at: new Date().toISOString() })
    .eq('id', sessionId)
    .is('deleted_at', null);  // idempotent: don't bump deleted_at on re-deletes
  if (error) throw error;
}

// ── Messages ────────────────────────────────────────────────────────────

export async function addMessage(sessionId, msg) {
  const row = {
    session_id: sessionId,
    message_index: msg.message_index,
    role: msg.role,
    content: msg.content ?? null,
    tool_name: msg.tool_name ?? null,
    tool_use_id: msg.tool_use_id ?? null,
    tool_input: msg.tool_input ?? null,
    tool_result: msg.tool_result ?? null,
    attachments: msg.attachments?.length ? msg.attachments : null,
  };
  const { data, error } = await supabase
    .from('autopilot_messages')
    .insert(row)
    .select()
    .single();
  if (error) throw error;
  return data;
}

export async function addMessages(sessionId, messages) {
  if (!messages.length) return [];
  const rows = messages.map(msg => ({
    session_id: sessionId,
    message_index: msg.message_index,
    role: msg.role,
    content: msg.content ?? null,
    tool_name: msg.tool_name ?? null,
    tool_use_id: msg.tool_use_id ?? null,
    tool_input: msg.tool_input ?? null,
    tool_result: msg.tool_result ?? null,
    attachments: msg.attachments?.length ? msg.attachments : null,
  }));
  const { data, error } = await supabase
    .from('autopilot_messages')
    .insert(rows)
    .select();
  if (error) throw error;
  return data || [];
}

export async function getMessages(sessionId) {
  const { data, error } = await supabase
    .from('autopilot_messages')
    .select('*')
    .eq('session_id', sessionId)
    .order('message_index', { ascending: true });
  if (error) throw error;
  return data || [];
}

export async function getNextMessageIndex(sessionId) {
  const { data, error } = await supabase
    .from('autopilot_messages')
    .select('message_index')
    .eq('session_id', sessionId)
    .order('message_index', { ascending: false })
    .limit(1);
  if (error) throw error;
  return data?.[0]?.message_index != null ? data[0].message_index + 1 : 0;
}

// ── OpenAI message reconstruction ───────────────────────────────────────
// Reuses the same contract as the legacy orchestrator repo: each stored row
// maps back to an OpenAI /chat/completions message. Handles multi-tool-call
// turns and image attachments.

function attachmentToImageBlock(att) {
  if (!att?.url) return null;
  const ct = att.content_type || '';
  if (!ct.startsWith('image/')) return null;
  return { type: 'image_url', image_url: { url: att.url } };
}

export async function getMessagesForLLM(sessionId) {
  const rows = await getMessages(sessionId);
  const result = [];

  // 历史压缩：记录最近一条"assistant text"消息在 result 数组里的索引，
  // 当随后遇到一个成功的 persist_stage_output tool_result 时，把那条消息的
  // content 替换为 tool_result.compressed_text，原始长 markdown 从此不进 LLM。
  // 数据库里原 assistant row 不动，UI 仍能完整渲染历史。
  let lastAssistantTextIdx = -1;

  for (const row of rows) {
    if (row.role === 'assistant' && row.tool_use_id) {
      // Merge consecutive assistant rows with tool_use_id into one message
      const toolCall = {
        id: row.tool_use_id,
        type: 'function',
        function: { name: row.tool_name, arguments: JSON.stringify(row.tool_input || {}) },
      };
      const last = result[result.length - 1];
      if (last && last.role === 'assistant' && Array.isArray(last.tool_calls)) {
        last.tool_calls.push(toolCall);
      } else {
        result.push({ role: 'assistant', content: row.content || null, tool_calls: [toolCall] });
      }
    } else if (row.role === 'tool') {
      // 检测 persist_stage_output 成功 → 压缩最近一条 assistant text
      if (
        row.tool_name === 'persist_stage_output'
        && row.tool_result?.ok
        && typeof row.tool_result?.compressed_text === 'string'
        && lastAssistantTextIdx >= 0
        && result[lastAssistantTextIdx]
        && result[lastAssistantTextIdx].role === 'assistant'
        && !Array.isArray(result[lastAssistantTextIdx].tool_calls)
      ) {
        result[lastAssistantTextIdx].content = row.tool_result.compressed_text;
        // 压缩完一次后清零，避免后续 persist 误压缩到已压缩的同一条
        lastAssistantTextIdx = -1;
      }
      const content = row.tool_result != null ? JSON.stringify(row.tool_result)
        : (row.content || '[no result]');
      result.push({ role: 'tool', tool_call_id: row.tool_use_id, content });
    } else if (row.role === 'user') {
      const imageBlocks = (row.attachments || []).map(attachmentToImageBlock).filter(Boolean);
      if (imageBlocks.length) {
        const content = [];
        if (row.content) content.push({ type: 'text', text: row.content });
        content.push(...imageBlocks);
        result.push({ role: 'user', content });
      } else if (row.content) {
        result.push({ role: 'user', content: row.content });
      }
    } else if (row.role === 'assistant' && row.content) {
      result.push({ role: 'assistant', content: row.content });
      // 记下这条 assistant text，下一个 persist_stage_output 成功就替换它
      lastAssistantTextIdx = result.length - 1;
    }
    // Rows with empty content + no tool_use_id are skipped (OpenAI rejects them).
  }

  return sanitizeToolCallPairs(result);
}

// Ensure every assistant tool_call has a matching {role:'tool'} follow-up.
// Crashes mid-execution can leave orphaned tool_use rows in the DB; this
// patches both directions so the next LLM call doesn't 400.
function sanitizeToolCallPairs(messages) {
  const out = [];
  for (let i = 0; i < messages.length; i++) {
    const msg = messages[i];
    out.push(msg);
    if (msg.role !== 'assistant' || !Array.isArray(msg.tool_calls) || !msg.tool_calls.length) continue;

    const callIds = new Set(msg.tool_calls.map(tc => tc.id));
    let j = i + 1;
    const followingToolIdxs = [];
    while (j < messages.length && messages[j].role === 'tool') {
      followingToolIdxs.push(j);
      j++;
    }
    const existingResultIds = new Set(followingToolIdxs.map(k => messages[k].tool_call_id));

    for (const id of callIds) {
      if (!existingResultIds.has(id)) {
        // The original tool call started but no tool_result was persisted —
        // either the previous turn crashed mid-execution or the SSE stream
        // was aborted. The side effect (image generated + uploaded, Meta
        // resource created, …) may already be done; replaying the call could
        // duplicate it. Tell the model not to retry and to ask the user how
        // to proceed instead.
        out.push({
          role: 'tool',
          tool_call_id: id,
          content: '[tool execution was interrupted by a previous crash or disconnect. The side effect may or may not have completed. DO NOT call this tool again with the same arguments — instead, briefly summarize what was attempted and ask the user whether to retry or move on.]',
        });
      }
    }
    for (const k of followingToolIdxs) {
      if (callIds.has(messages[k].tool_call_id)) out.push(messages[k]);
    }
    i = j - 1;
  }
  return out;
}
